import java.util.Scanner;

public class ExerciaDiaSeis7 {

	public static void main(String[] args) {
		/*(for) Escreva um programa Java para imprimir natela todos os números
ímpares de 299 a 101 em ordem decrescente*/
		for(int i=299; i>100; i--) {
			if(i%2 != 0) {
				System.out.println(i);
			}
		}
	}
}
